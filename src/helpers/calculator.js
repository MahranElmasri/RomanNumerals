const calculator = {
  add: (firstNum, secondNum) => {
    return firstNum + secondNum;
  },
  sub: (firstNum, secondNum) => {
    return firstNum - secondNum;
  },
  mul: (firstNum, secondNum) => {
    return firstNum * secondNum;
  },
  div: (firstNum, secondNum) => {
    return firstNum / secondNum;
  }
};
export default calculator;
